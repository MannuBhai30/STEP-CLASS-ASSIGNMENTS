class Q1 {
    public static void main(String[] args) {
        
        // Create a string variable name and assign value Harry
        String name = "Harry";
        
        // Create an int variable birthYear and assign value 2000 
        int birthYear = 2000;
        
        // Create an int variable currentYear and assign value 2024 
        int currentYear = 2024;
        
        // Calculate age by subtracting birthYear from currentYear 
        int age = currentYear - birthYear;
        
        // Display the result in the format: Harry's age in 2024 is ___ 
        System.out.println(name + "'s age in " + currentYear + " is " + age);
    }
}